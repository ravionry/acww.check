const STORAGE_KEY = "ac-progress";

function loadProgress() {
  try {
    return JSON.parse(localStorage.getItem(STORAGE_KEY)) || {};
  } catch {
    return {};
  }
}

function saveProgress() {
  const data = {};

  Object.keys(datasets).forEach(category => {
    data[category] = {};
    datasets[category].forEach(item => {
      data[category][item.id] = item.obtained;
    });
  });

  localStorage.setItem(STORAGE_KEY, JSON.stringify(data));
}

(function hydrateData() {
  const saved = loadProgress();

  Object.keys(datasets).forEach(category => {
    datasets[category].forEach(item => {
      if (saved?.[category]?.[item.id] !== undefined) {
        item.obtained = saved[category][item.id];
      }
    });
  });
})();

